class Solution {
    public int uniquePaths(int m, int n) {
        long numerator = 1; // åˆ†å­
        int denominator = m - 1; // åˆ†æ¯
        int count = m - 1;
        int t = m + n - 2;
        for (; count>0;count--) {
            numerator *= (t--);
            while (denominator != 0 && numerator % denominator == 0) {
                numerator /= denominator;
                denominator--;
            }
        }
        return (int)numerator;
    }
}
