class Solution {
    public boolean isValidSudoku(char[][] board) {
        int n = board.length;
        //æ£€æŸ¥æ¯ä¸€è¡Œæ˜¯å¦æœ‰é‡å¤
        for (int i = 0; i < n; i++){
            Set<Character> set = new HashSet<>();
            for (int j = 0; j < n; j++){
                if (board[i][j] != '.'){
                    if (set.contains(board[i][j])){
                        return false;
                    }
                    set.add(board[i][j]);
                }
            }
        }
        //æ£€æŸ¥æ¯ä¸€åˆ—æ˜¯å¦æœ‰é‡å¤
        for (int i = 0; i < n; i++){
            Set<Character> set = new HashSet<>();
            for (int j = 0; j < n; j++){
                if (board[j][i] != '.'){
                    if (set.contains(board[j][i])){
                        return false;
                    }
                    set.add(board[j][i]);
                }
            }
        }
        //æ£€æŸ¥æ¯ä¸€ä¸ªä¹å®«æ ¼æ˜¯å¦æœ‰é‡å¤
        for (int i =0; i < 3; i++){
            for (int j = 0; j < 3; j++){
                int rowStart = i*3;
                int colStart = j*3;
                Set<Character> set = new HashSet<>();
                for (int k = 0; k < 3; k++){
                    for (int l = 0; l < 3; l++){
                        if (board[rowStart+k][colStart+l] != '.'){
                            if (set.contains(board[rowStart+k][colStart+l] )){
                                return false;
                            }
                            set.add(board[rowStart+k][colStart+l] );
                        }
                    }
                }

            }
        }
        return true;
    }
}
